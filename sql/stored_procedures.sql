    BEGIN
        THROW 50000, N'Số tiết lý thuyết và thực hành không được âm.', 1;
    END;

    UPDATE dbo.MONHOC
    SET TENMH = @TENMH,
        SOTIET_LT = @SOTIET_LT,
        SOTIET_TH = @SOTIET_TH
    WHERE MAMH = @MAMH;
END;
GO

