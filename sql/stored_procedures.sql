CREATE OR ALTER PROCEDURE dbo.SP_DanhSachLopTinChi
    @MAKHOA NCHAR(10) = NULL
AS
BEGIN
    SET NOCOUNT ON;

    SELECT LTC.*, MH.TENMH, GV.HO + N' ' + GV.TEN AS HOTENGV,
           (SELECT COUNT(*)
            FROM dbo.DANGKY AS DK
            WHERE DK.MALTC = LTC.MALTC
              AND (DK.HUYDANGKY = 0 OR DK.HUYDANGKY IS NULL)) AS SOSVDK
    FROM dbo.LOPTINCHI AS LTC
    JOIN dbo.MONHOC AS MH ON LTC.MAMH = MH.MAMH
    JOIN dbo.GIANGVIEN AS GV ON LTC.MAGV = GV.MAGV
    WHERE @MAKHOA IS NULL OR LTC.MAKHOA = @MAKHOA
    ORDER BY LTC.NIENKHOA DESC, LTC.HOCKY, MH.TENMH, LTC.NHOM;
END;
GO

CREATE OR ALTER PROCEDURE dbo.SP_ThemLopTinChi
    @NIENKHOA NCHAR(9),
    @HOCKY INT,
    @MAMH NCHAR(10),
    @NHOM INT,
    @MAGV NCHAR(10),
    @MAKHOA NCHAR(10),
    @SOSVTOITHIEU INT
AS
BEGIN
    SET NOCOUNT ON;

    IF NOT EXISTS (SELECT 1 FROM dbo.MONHOC WHERE MAMH = @MAMH)
    BEGIN
        THROW 50000, N'Môn học không tồn tại.', 1;
    END;

    IF NOT EXISTS (SELECT 1 FROM dbo.GIANGVIEN WHERE MAGV = @MAGV)
    BEGIN
        THROW 50000, N'Giảng viên không tồn tại.', 1;
    END;

    IF NOT EXISTS (SELECT 1 FROM dbo.KHOA WHERE MAKHOA = @MAKHOA)
